
// For Loop
for (let i = 0; i < 5; i++) {
    console.log("For loop iteration:", i);
}

// While Loop
let count = 0;
while (count < 3) {
    console.log("While loop count:", count);
    count++;
}

// Do-While Loop
let num = 0;
do {
    console.log("Do-while loop number:", num);
    num++;
} while (num < 2);
